"""
= 赋值  a=10
+= 加法赋值 a+=10 等同于 a=a+10
-= 减法赋值 a-=10 等同于 a=a-10
*= 乘法赋值 a*=10 等同于 a=a*10
/= 除法赋值 a/=10 等同于 a=a/10
%= 取模赋值 a+=10 等同于 a=a%10
**= 幂等赋值  a**=10 等同于 a= a**10
//= 取除数赋值符 a//=10 等同于 a = a//10
"""
a = 2
b = 3
c = 0

c = a + b
print("1. c = ", c)

c += a
print("2. c = ", c)

c *= a
print("3. c = ", c)

c /= a
print("4. c = ", c)

c = 11
c %= a
print("5. c = ", c)

c **=a
print("6. c = ", c)

c //=a
print("7. c = ", c)
